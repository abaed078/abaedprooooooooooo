export function searchDtcDatabase(query: string): DtcEntry[] {
  const q = query.toUpperCase().trim();
  if (!q) return [];
  return DTC_DATABASE.filter(entry =>
    entry.code.includes(q) ||
    entry.description.toUpperCase().includes(q) ||
    (entry.descriptionAr && entry.descriptionAr.includes(query)) ||
    entry.system.toUpperCase().includes(q) ||
    (entry.systemAr && entry.systemAr.includes(query)) ||
    entry.causes.some(c => c.toUpperCase().includes(q)) ||
    (entry.causesAr && entry.causesAr.some(c => c.includes(query)))
  );
}
